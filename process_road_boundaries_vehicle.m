x_center = centerline_data(:,1);
y_center = centerline_data(:,2);
n_points = length(x_center);

% ROAD GEOMETRY PARAMETERS
% --------------------------
lane_width = 3.6;      % meters - from centerline to asphalt edge
shoulder_width = 1.0;  % meters - soil/unpaved area outside asphalt
total_width = lane_width + shoulder_width;  % Total safe boundary

% Initialize boundary arrays
x_asphalt_left = zeros(n_points, 1);
y_asphalt_left = zeros(n_points, 1);
x_asphalt_right = zeros(n_points, 1);
y_asphalt_right = zeros(n_points, 1);

x_outer_left = zeros(n_points, 1);
y_outer_left = zeros(n_points, 1);
x_outer_right = zeros(n_points, 1);
y_outer_right = zeros(n_points, 1);

heading = zeros(n_points, 1);

% Calculate boundaries for each point
for i = 1:n_points
    if i == 1
        % Forward difference for first point
        dx = x_center(i+1) - x_center(i);
        dy = y_center(i+1) - y_center(i);
    elseif i == n_points
        % Backward difference for last point
        dx = x_center(i) - x_center(i-1);
        dy = y_center(i) - y_center(i-1);
    else
        % Central difference for middle points
        dx = x_center(i+1) - x_center(i-1);
        dy = y_center(i+1) - y_center(i-1);
    end
    
    % Normalize tangent vector
    tangent_length = sqrt(dx^2 + dy^2);
    dx = dx / tangent_length;
    dy = dy / tangent_length;
    
    % Calculate heading angle (in radians)
    heading(i) = atan2(dy, dx);
    
    % Perpendicular vector (rotate tangent 90 degrees)
    % Left boundary (rotate CCW)
    perp_x_left = -dy;
    perp_y_left = dx;
    
    % Right boundary (rotate CW)
    perp_x_right = dy;
    perp_y_right = -dx;
    
    % Calculate ASPHALT edge boundaries (3.6m from center)
    x_asphalt_left(i) = x_center(i) + lane_width * perp_x_left;
    y_asphalt_left(i) = y_center(i) + lane_width * perp_y_left;
    
    x_asphalt_right(i) = x_center(i) + lane_width * perp_x_right;
    y_asphalt_right(i) = y_center(i) + lane_width * perp_y_right;
    
    % Calculate OUTER boundaries (4.6m from center - asphalt + shoulder)
    x_outer_left(i) = x_center(i) + total_width * perp_x_left;
    y_outer_left(i) = y_center(i) + total_width * perp_y_left;
    
    x_outer_right(i) = x_center(i) + total_width * perp_x_right;
    y_outer_right(i) = y_center(i) + total_width * perp_y_right;
end

% Create road boundary structure
RoadBoundaries.centerline.x = x_center;
RoadBoundaries.centerline.y = y_center;
RoadBoundaries.centerline.heading = heading;

% Asphalt boundaries (main road surface)
RoadBoundaries.asphalt_left.x = x_asphalt_left;
RoadBoundaries.asphalt_left.y = y_asphalt_left;
RoadBoundaries.asphalt_right.x = x_asphalt_right;
RoadBoundaries.asphalt_right.y = y_asphalt_right;

% Outer boundaries (including shoulder)
RoadBoundaries.outer_left.x = x_outer_left;
RoadBoundaries.outer_left.y = y_outer_left;
RoadBoundaries.outer_right.x = x_outer_right;
RoadBoundaries.outer_right.y = y_outer_right;

% Store parameters
RoadBoundaries.lane_width = lane_width;
RoadBoundaries.shoulder_width = shoulder_width;
RoadBoundaries.total_width = total_width;

